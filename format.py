def format_text_with_newline(raw_str):
    """
    将多行原始字符串格式化为 > "文本" 格式，每行独立且自带换行

    Args:
        raw_str (str): 包含多行文本的原始字符串（换行分隔）

    Returns:
        str: 格式化后的文本（每行带换行）
    """
    formatted_lines = []
    # 按换行拆分并处理每行
    lines = raw_str.split('\n')
    for line in lines:
        clean_line = line.strip()
        if clean_line:  # 过滤空行
            escaped_line = clean_line.replace('"', '\\"')
            formatted_line = f'> "{escaped_line}"'
            formatted_lines.append(formatted_line)
    # 用换行符拼接所有行，最终结果自带换行
    return '\n\n'.join(formatted_lines)  # 两行之间加空行，匹配你示例的排版


